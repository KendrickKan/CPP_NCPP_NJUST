template <typename T>
void read(T &t)
{
    t = 0;
    char ch = getchar();
    int f = 1;
    while (ch < '0' || ch > '9')
    {
        if (ch == '-')
            f = -1;
        ch = getchar();
    }
    do
    {
        (t *= 10) += ch - '0';
        ch = getchar();
    } while ('0' <= ch && ch <= '9');
    t *= f;
}
typedef long long ll;
typedef pair<int, int> pii;
typedef pair<ll, ll> pll;
ll gcd(ll x, ll y)
{
    return y == 0 ? x : gcd(y, x % y);
}
int getsum(ll x)
{
    int sum = 0;
    while (x > 0)
    {
        sum += x % 10;
        x /= 10;
    }
    return sum;
}
int main()
{
    FastIO;
    int t;
    read(t);
    while (t--)
    {
        ll n;
        read(n);
        while (gcd(n, getsum(n)) == 1)
        {
            n++;
        }
        cout << n << endl;
    }
    return 0;
}